def create_failure_visualizations(failed_materials, spg_failure_df, result_df, match_columns):
    """
    Create additional visualizations for failure analysis
    """
    fig, axes = plt.subplots(2, 2, figsize=(15, 12))
    fig.suptitle('Failure Analysis', fontsize=16)
    
    # 1. Failure rate by space group (top 20)
    ax1 = axes[0, 0]
    top_failing_spgs = spg_failure_df.head(20)
    bars = ax1.bar(range(len(top_failing_spgs)), top_failing_spgs['failure_rate'])
    ax1.set_xticks(range(len(top_failing_spgs)))
    ax1.set_xticklabels(top_failing_spgs.index, rotation=45, ha='right')
    ax1.set_ylabel('Failure Rate (%)')
    ax1.set_title('Top 20 Space Groups by Failure Rate')
    
    # Color bars by failure rate
    for i, bar in enumerate(bars):
        if top_failing_spgs['failure_rate'].iloc[i] == 100:
            bar.set_color('darkred')
        elif top_failing_spgs['failure_rate'].iloc[i] > 75:
            bar.set_color('red')
        elif top_failing_spgs['failure_rate'].iloc[i] > 50:
            bar.set_color('orange')
    
    # 2. Distribution of failures across generations
    ax2 = axes[0, 1]
    failed_by_gen = []
    for col in match_columns:
        n_failed = len(result_df[result_df[col] == 0])
        failed_by_gen.append(n_failed)
    
    ax2.plot(range(1, len(match_columns)+1), failed_by_gen, 'o-', linewidth=2, markersize=8)
    ax2.set_xlabel('Generation')
    ax2.set_ylabel('Number of Failed Predictions')
    ax2.set_title('Failures per Generation')
    ax2.grid(True, alpha=0.3)
    
    # 3. Complexity vs failure rate
    ax3 = axes[1, 0]
    complexity_data = result_df.copy()
    complexity_data['n_elements'] = complexity_data['Groundtruth Composition'].apply(
        lambda x: len(set([c for c in x if c.isupper()]))
    )
    complexity_data['failed'] = complexity_data[match_columns].sum(axis=1) == 0
    
    complexity_stats = complexity_data.groupby('n_elements')['failed'].agg(['sum', 'count'])
    complexity_stats['failure_rate'] = complexity_stats['sum'] / complexity_stats['count'] * 100
    
    ax3.bar(complexity_stats.index, complexity_stats['failure_rate'])
    ax3.set_xlabel('Number of Elements in Composition')
    ax3.set_ylabel('Failure Rate (%)')
    ax3.set_title('Failure Rate vs Composition Complexity')
    
    # 4. Heatmap of common failing element combinations
    ax4 = axes[1, 1]
    
    # Extract element pairs from failed compositions
    element_pairs = {}
    for comp in failed_materials['Groundtruth Composition']:
        elements = []
        i = 0
        while i < len(comp):
            if comp[i].isupper():
                elem = comp[i]
                if i + 1 < len(comp) and comp[i + 1].islower():
                    elem += comp[i + 1]
                    i += 1
                elements.append(elem)
            i += 1
        
        # Count pairs
        for i in range(len(elements)):
            for j in range(i+1, len(elements)):
                pair = tuple(sorted([elements[i], elements[j]]))
                element_pairs[pair] = element_pairs.get(pair, 0) + 1
    
    # Get top pairs
    top_pairs = sorted(element_pairs.items(), key=lambda x: x[1], reverse=True)[:10]
    pair_text = [f"{p[0][0]}-{p[0][1]}" for p in top_pairs]
    pair_counts = [p[1] for p in top_pairs]
    
    ax4.barh(pair_text, pair_counts)
    ax4.set_xlabel('Frequency in Failed Predictions')
    ax4.set_title('Most Common Element Pairs in Failures')
    
    plt.tight_layout()
    return fig

