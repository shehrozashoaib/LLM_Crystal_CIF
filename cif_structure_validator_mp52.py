def process_csv(input_csv, output_csv):
    df = pd.read_csv(input_csv)
    generation_columns = [col for col in df.columns if col.startswith("generation_")]
    
    # ADD: Logging
    logging.info(f"Processing {len(df)} samples with {len(generation_columns)} generations each")
    logging.info(f"Input file: {input_csv}")

    results = []
    # CHANGE: Replace df.iterrows() with tqdm for progress bar
    for idx, row in tqdm(df.iterrows(), total=len(df), desc="Validating structures"):
        # REMOVE the old print statement
        # print(f"Processing row {idx + 1} of {len(df)}")
        
        material_id = row['material_id']
        ground_truth_cif = row['output']

        valid_gt, struct_gt, gt_composition, gt_spg = validate_structure(ground_truth_cif)
        
        # CHANGE: Use logging instead of print
        logging.debug(f"Material {material_id} - GT: {gt_composition}")

        result = {
            "material_id": material_id,
            "Groundtruth Composition": gt_composition,
            "Groundtruth SPG": gt_spg
        }

        for gen_col in generation_columns:
            generated_cif = row[gen_col]
            valid_gen, struct_gen, gen_composition, gen_spg = validate_structure(generated_cif)

            result[f"Validity_{gen_col}"] = "valid" if valid_gen else "invalid"
            result[f"Generated Composition_{gen_col}"] = gen_composition
            result[f"Generated SPG_{gen_col}"] = gen_spg

            if valid_gt and valid_gen:
                rms_distance = compare_structures(struct_gen,struct_gt)
                match = 1 if isinstance(rms_distance, float) and not np.isnan(rms_distance) else 0
                result[f"RMS Distance_{gen_col}"] = rms_distance
                result[f"Match_{gen_col}"] = match
                result[f"Error_{gen_col}"] = ""
                # Near-miss RMS: strict RMS if matched, else loose "how close" RMS.
                # Gives a continuous error signal for EVERY valid generation.
                result[f"NearMiss RMS_{gen_col}"] = (
                    rms_distance if match else near_miss_rms(struct_gen, struct_gt)
                )
            else:
                result[f"RMS Distance_{gen_col}"] = np.nan
                result[f"Match_{gen_col}"] = 0
                result[f"Error_{gen_col}"] = "Invalid CIF"
                result[f"NearMiss RMS_{gen_col}"] = np.nan

        results.append(result)

    result_df = pd.DataFrame(results)
    result_df.to_csv(output_csv, index=False, encoding="utf-8")
    
    match_columns = [f"Match_{col}" for col in generation_columns]
    any_match = result_df[match_columns].any(axis=1).sum()
    success_rate = 100 * any_match / len(result_df)

    failed_materials, spg_failure_df = analyze_failures(result_df, match_columns)
    failure_fig = create_failure_visualizations(failed_materials, spg_failure_df, result_df, match_columns)
    # Save failure analysis results
    failed_materials.to_csv(f'{output_dir}/failed_materials_analysis.csv', index=False)
    spg_failure_df.to_csv(f'{output_dir}/space_group_failure_rates.csv')

    # Save failure visualization
    failure_fig.savefig(f'{output_dir}/failure_analysis.png', dpi=300, bbox_inches='tight')

    # Add failure summary to your main summary
    with open(f'{output_dir}/validation_summary_with_failures.txt', 'w') as f:
        f.write(f"Validation Summary\n")
        f.write(f"Generated on: {datetime.now()}\n")
        f.write(f"Total samples: {len(result_df)}\n")
        f.write(f"Success rate: {success_rate:.1f}%\n\n")
        
        f.write(f"FAILURE ANALYSIS\n")
        f.write(f"="*50 + "\n")
        f.write(f"Complete failures: {len(failed_materials)}/{len(result_df)} ({len(failed_materials)/len(result_df)*100:.1f}%)\n\n")
        
        f.write(f"Top 5 Failing Space Groups:\n")
        for spg, data in spg_failure_df.head(5).iterrows():
            f.write(f"  {spg}: {data['failure_rate']:.1f}% ({data['failed']}/{data['total']})\n")
        
        f.write(f"\nMost Problematic Compositions:\n")
        for comp, count in failed_materials['Groundtruth Composition'].value_counts().head(5).items():
            f.write(f"  {comp}: {count} failures\n")

    print(f"\nFailure analysis complete! Check '{output_dir}/failed_materials_analysis.csv' for details.")
    # ADD: Log completion
    logging.info(f"Results saved to {output_csv}")
    
    # ADD: Call summary function
    print_summary_statistics(result_df, generation_columns)
    create_validation_plots(result_df, os.path.dirname(output_csv))
    
    logging.info(f"Memory usage after validation: {psutil.Process().memory_info().rss / 1024 ** 2:.2f} MB")

    return result_df

