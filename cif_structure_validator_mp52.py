    def count_elements(comp_series):
        element_counts = {}
        for comp_str in comp_series:
            try:
                comp = Composition(comp_str)  
                for element in comp.elements:
                    elem_symbol = element.symbol
                    element_counts[elem_symbol] = element_counts.get(elem_symbol, 0) + 1
            except:
                pass
        return element_counts
    
    failed_elements = count_elements(failed_materials['Groundtruth Composition'])
    successful_materials = result_df[result_df[match_columns].sum(axis=1) > 0]
    success_elements = count_elements(successful_materials['Groundtruth Composition'])
    
    # Find elements that appear more in failures
    problematic_elements = []
    for elem in failed_elements:
        if elem in success_elements:
            fail_ratio = failed_elements[elem] / (failed_elements[elem] + success_elements[elem])
            if fail_ratio > 0.7:  # Elements that fail >70% of the time
                problematic_elements.append((elem, fail_ratio, failed_elements[elem]))
    
    print("\nProblematic Elements (>70% failure rate):")
    for elem, ratio, count in sorted(problematic_elements, key=lambda x: x[1], reverse=True):
        print(f"  {elem}: {ratio*100:.1f}% failure rate ({count} failures)")
    
    # Analyze by number of elements in composition
