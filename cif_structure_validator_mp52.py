"""
CIF Structure Validator
----------------------
Validates crystal structures generated by ML models against ground truth data from Materials Project.

What it does:
- Validates CIF files for chemical and structural validity
- Compares generated structures to ground truth using pymatgen's StructureMatcher
- Generates comprehensive statistics and visualizations
- Tracks success rates across multiple generation attempts

Input: CSV with columns [material_id, output (ground truth CIF), generation_0, generation_1, ...]
Output: 
  - CSV with validation results and RMS distances
  - Summary text file with statistics
  - 6 visualization plots (validity rates, RMS distribution, etc.)
  - Log file with processing details

Usage:
    python cif_structure_validator.py

Key parameters:
    - Structure matching: stol=0.5, ltol=0.3, angle_tol=10 (in compare_structures)
    - Validity cutoff: 0.5 Å minimum atomic distance
    - Minimum volume: 0.1 Å³
    
The script expects:
    - Input CSV in {output_dir}/ directory
    - pymatgen, pandas, numpy, matplotlib, seaborn, tqdm installed
    - ~2GB RAM per 1000 structures
    - ~2-5 minutes per 1000 structures

For different models/datasets, modify:
    - input_csv: path to your generated structures CSV
    - output_csv: naming convention for results
    - matching tolerances if structures are less/more precise
"""

